        </li>
        <li>Foster a growing number of professionally trained religious individuals for the benefit of common rural
            people.
        </li>
        <li>
            Protect common people from hazards such as threats, harassments, litigation, etc. from the external
            influences and manipulations.
        </li>
        <li>
            Support people who are passionate about becoming a religious person.
        </li>
        <li>
            Promote interaction and exchange of ideas for the professional development of its members.
        </li>
        <li>
            Explore funding sources to collaborate and invest in professional programs for spiritual and religious
            development.
        </li>
        <li>
            Institute mechanism to recognise a religious person through awards and grants.
        </li>
        <li>
            Stimulate and sustain professional debate on crucial issues through seminars, workshops and discussion
            forum.
        </li>
        <li>
            Maintain and promote a high standard of ethical behaviour in the practice of <strong>BKWC
                Foundation.</strong>
        </li>
    </ul>
    </p>

