    <h4 id="title" class="text-center my-3" data-aos="fade-up" data-aos-duration="1000">
        Objectives
    </h4>

    <p style="margin:0 10% 0 10%; line-height: 29px;" class="text-secondary" data-aos="fade-up"
        data-aos-duration="1000">
        The objectives of the Foundation are to serve as a common platform & a national point of reference for all the
        Foundation members in Bhutan and to serve as a forum and point of contact on policy matters, legislation, and
        regulations affecting the religious organization and its activities & to interface with government agencies and
        other authorities on issues concerning the religious organization and it's promotions. The objectives of the
        Foundation are, inter alia, to:
    <ul style="margin:0 10% 0 10%;" style="list-style-type: square;" class="text-secondary" data-aos="fade-up"
        data-aos-duration="1000">
        <li>Establish & promote code of ethics, formulate strategies and legislation to accelerate the promotion of
            religious organization and its activities in Bhutan;
        </li>
        <li>
            Foster friendship and promote networking of Foundation members;
        </li>
        <li>Facilitate and promote cooperation among religious and non-religious members and other stakeholders within
            country and develop global cooperation and establish interaction links with neighbouring countries and
            international level be at an individual, group or institutional level;
        </li>
        <li>
            Promote exchanging ideas, knowledge and information amongst Foundation members and participate to share with
            regional neighbouring and global countries;
        </li>
        <li>Support common people engagement and communities of religious practice and its development for the benefit
            and wellbeing of the society;
        </li>
        <li>Cultivate leadership, support and promote public diplomacy;
        </li>
        <li>Mediate & arbitrate (resolve), represent, foster, develop, encourage and improve the religious organization.
        </li>
        <li>Promote and support regional fund raising activities with other Religious Organization, NGO and Foundations
            to contribute social benefits.
        </li>
    </ul>
    </p>

    <br>
    <h4 id="title" class="text-center my-3" data-aos="fade-up" data-aos-duration="1000">
        Scope
    </h4>

    <p style="margin:0 10% 0 10%; line-height: 29px;" class="text-secondary" data-aos="fade-up"
        data-aos-duration="1000">
        The Foundation mandates to create a database of Foundation members with relevant information about their
        contact, education background and work experiences so that relevant information pertaining to the development
        and contribution towards the preservations and promotion of religious organization and its activities in Bhutan
        are maintained and any other matters of interest could be timely disseminated accordingly. The scope of
        activities of the Foundation shall comprise any activities that contribute to the achievement of the
        Foundation's purpose and objectives, and may include, without limitation:
    <ul style="margin:0 10% 0 10%;" style="list-style-type: square;" class="text-secondary" data-aos="fade-up"
        data-aos-duration="1000">
        <li>Represent Foundation members both within and outside the country.
        </li>
        <li>Integration activities for all Foundation members;
        </li>
        <li>Revolutionize the profession of Foundation members to earn respect, support and admiration from the
            communities;
        </li>
        <li>Mentoring activities and counselling of young aspiring and volunteers for the participation in the religious
            organization and its development;
        </li>
        <li>Support and develop religious arts and related skills to promote talents and preserve Bhutan's unique
            traditional and cultural heritage. Take necessary steps to promote, encourage and assist in the development
            of the religious artists in Bhutan;
        </li>
        <li>Promotional and social activities, organise workshops, trainings and seminars;
        </li>
        <li>Facilitate services and resources to the members of BKWC Foundation to achieve the common goal and
            objectives of religious activities and it's promotions;
        </li>
        <li>To communicate and act as a liaison with the Government, national and international organizations,
            Foundations and other mercantile to promote measures in the interest of the religious members;
        </li>
        <li>Institute a regional administration offices or a religious stadium as deemed required for all aspiring
            Foundation and religious members to learn, educate religions, develop skills and performance that will
            attain an individual member's career sustainability and the source of income;
        </li>
        <li>Participation in the regional countries and international religious markets;
        </li>
        <li>Participate in the international markets through the adaptation and diversification of religion innovations
            and products. Obtain affiliation with similar organizations in other countries.
        </li>
        <li>Promote our Buddhist religions among youth, facilitate the development of the religious art into a
            profitable and dignified vocation and career in order to contribute enhancing the social livelihood and a
            source of income;</li>
        <li>Establish platform for collaboration amongst those aspiring youth talents, innovations and showcase the
            religious art and literature.
        </li>
        <li>Institute awards for excellence in Buddhist art and literature and related activities. Organize and promote
            members award and event functions to celebrate/recognize members talent and achievements;
        </li>
        <li>Organize exhibitions, fund raising activities and religious fairs to promote our culture, skills,
            performance and the potential of the Foundation members to generate fund and income;
        </li>
        <li>Provide avenue, market and receive religious event contracts from the Government, corporate and private
            organizations for the members of the Foundation (Source of income for Foundation members);
        </li>
        <li>Organise and manage the activities of Foundation members for religious contract and event hosting;
        </li>
        <li>Initiate programs that develop and promote the educational, cultural, and vocational relationship with
            neighbouring countries;
        </li>
        <li>Promoting all lawful activities that may be incidental or reasonably necessary to achieve the purposes of
            the Foundation.
        </li>
    </ul>
    </p>
