        BAYUEL KINZANG WOESEL CHOLING FOUNDATION
    </h4>
    <br>
    <p class="text-secondary" data-aos="fade-up" data-aos-duration="1000" style="margin:0 10% 0 10%;">
        The name of this Foundation is "Baeyuel Kuenzang Woesel Choeling Foundation " and is a Religious Organization, a
        non-profit organization established to develop as a Spiritual and Cultural Centre in order to preserve, promote
        and propagate Buddha Dharma (Vajrayana) teachings of Tertoen Drukdra Dorji Yangsi, His Holiness Trulku Ugyen
        Drodrul Thinley Kunchap, who was formally recognised and enthroned by the present 70th Je Khenpo Jigme Chodra
        and the Central Monastic Body of Bhutan.
        <br>
        <br>
        The Organization under the Presidential of His Holiness Trulku Ugyen Drodrul Thinley Kunchap shall function to
        create peace, prosperity and wellbeing of rural communities of least developed and unite all religious
        individuals aspiring to take professionalism in promoting the spiritual welfare of sentient beings in the
        country and contribute the growth of Buddhist teaching and Buddha Dharma that will ultimately benefit our
        religious institutions and spiritual heritage of Bhutan.
        <br>
        <br>
        <strong>Baeyuel Kuenzang Woesel Choeling Foundation (BKWC Foundation)</strong>
        was registered on 17th November, 2020 under the
        Commission for Religious Organizations.
    </p>

